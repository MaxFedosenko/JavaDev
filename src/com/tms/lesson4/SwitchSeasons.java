package com.tms.lesson4;

public class SwitchSeasons {
    public void Seasons(MONTHS month) {
        switch (month) {
            case DECEMBER:
                System.out.println(month.season);
                break;
            case JANUARY:
                System.out.println(month.season);
                break;
            case FEBRUARY:
                System.out.println(month.season);
                break;
            case MARCH:
                System.out.println(month.season);
                break;
            case APRIL:
                System.out.println(month.season);
                break;
            case MAY:
                System.out.println(month.season);
                break;
            case JUNE:
                System.out.println(month.season);
                break;
            case JULE:
                System.out.println(month.season);
                break;
            case AUGUST:
                System.out.println(month.season);
                break;
            case SEPTEMBER:
                System.out.println(month.season);
                break;
            case OCTOBER:
                System.out.println(month.season);
                break;
            case NOVEMBER:
                System.out.println(month.season);
                break;
        }
    }
}
